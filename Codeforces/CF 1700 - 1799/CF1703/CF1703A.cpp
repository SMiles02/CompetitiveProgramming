#include <bits/stdc++.h>
using namespace std;

string to_upper(string a) {
    for (int i=0;i<(int)a.size();++i) if (a[i]>='a' && a[i]<='z') a[i]-='a'-'A';
    return a;
}

void solve() {
    string s;
    cin >> s;
    s = to_upper(s);
    if (s == "YES")
        cout << s << "\n";
    else
        cout << "NO\n";
}

int main() {
    ios_base::sync_with_stdio(0); cin.tie(0);
    int t;
    cin >> t;
    while (t--)
        solve();
    return 0;
}