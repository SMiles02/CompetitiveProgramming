string to_upper(string a) {
    for (int i=0;i<(int)a.size();++i) if (a[i]>='a' && a[i]<='z') a[i]-='a'-'A';
    return a;
}

string to_lower(string a) {
    for (int i=0;i<(int)a.size();++i) if (a[i]>='A' && a[i]<='Z') a[i]+='a'-'A';
    return a;
}

void solve() {
    int x, y, ans = 0;
    cin >> x >> y;
    while (x > 0 || y > 0) {
        ++ans;
        if (y > 1) {
            y -= 2;
            x -= 7;
        }
        else if (y == 1) {
            --y;
            x -= 11;
        }
        else {
            x -= 15;
        }
    }
    cout << ans << "\n";
}

int main() {
    ios_base::sync_with_stdio(0); cin.tie(0);
    int t;
    cin >> t;
    while (t--) {
        solve();
    }
    return 0;
}