string to_upper(string a) {
    for (int i=0;i<(int)a.size();++i) if (a[i]>='a' && a[i]<='z') a[i]-='a'-'A';
    return a;
}

string to_lower(string a) {
    for (int i=0;i<(int)a.size();++i) if (a[i]>='A' && a[i]<='Z') a[i]+='a'-'A';
    return a;
}

void solve() {
    int n, x = 1, y;
    cin >> n;
    y = n;
    // cout << n << "\n";
    for (int i = 0; i < n; ++i) {
        if (i & 1) {
            cout << y-- << " ";
        }
        else {
            cout << x++ << " ";
        }
    }
    cout << "\n";
}

int main() {
    ios_base::sync_with_stdio(0); cin.tie(0);
    int t;
    cin >> t;
    // cout << t << "\n";
    while (t--) {
        solve();
    }
    return 0;
}