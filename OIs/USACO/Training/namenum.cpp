void pGenerate(int cur, string curS)
{
    if (cur==n)
    {
        //cout<<curS<<"\n";
        if (m[curS])
        {
            v.push_back(curS);
            ++ans;
        }
        return;
    }
    if (s[cur]=='7')
    {
        pGenerate(cur+1,curS+'P');
        pGenerate(cur+1,curS+'R');
        pGenerate(cur+1,curS+'S');
        return;
    }
    int c;
    if ((s[cur]-'0')>7)
    {
        c=3*((s[cur]-'0')-8);
        c+='T';
        pGenerate(cur+1,curS+(char)c);
        ++c;
        pGenerate(cur+1,curS+(char)c);
        ++c;
        pGenerate(cur+1,curS+(char)c);
        return;
    }
    c=3*((s[cur]-'0')-2);
    c+='A';
    pGenerate(cur+1,curS+(char)c);
    ++c;
    pGenerate(cur+1,curS+(char)c);
    ++c;
    pGenerate(cur+1,curS+(char)c);
    return;
}

